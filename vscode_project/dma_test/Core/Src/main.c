  MX_GPIO_Init();
  MX_DMA_Init();
  /* USER CODE BEGIN 2 */

  // 配置dma传输参数
  HAL_DMA_Start(&hdma_memtomem_dma1_channel1, (uint32_t)src_data, (uint32_t)dest_data, DATA_LEN);

  // 等待dma传输完成
  HAL_DMA_PollForTransfer(&hdma_memtomem_dma1_channel1, HAL_DMA_FULL_TRANSFER, HAL_MAX_DELAY);

